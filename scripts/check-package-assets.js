#!/usr/bin/env node
// Verifies packaging metadata and required bundle assets before build:exe.

const fs = require('fs');
const path = require('path');
const { buildClientBundleSource } = require('./generate-client-bundle');

const ROOT = path.resolve(__dirname, '..');
let ok = true;

function pass(msg) {
  console.log(`OK   ${msg}`);
}

function fail(msg) {
  console.log(`FAIL ${msg}`);
  ok = false;
}

function readJson(rel) {
  return JSON.parse(fs.readFileSync(path.join(ROOT, rel), 'utf8'));
}

const pkg = readJson('package.json');
const lock = readJson('package-lock.json');

if (pkg.version === lock.version && pkg.version === lock.packages?.['']?.version) {
  pass(`package version aligned: ${pkg.version}`);
} else {
  fail(`version mismatch: package=${pkg.version}, lock=${lock.version}, root=${lock.packages?.['']?.version}`);
}

// pkg.assets globs are broken for node20 targets. package.json is still needed
// by pkg for version and main resolution.
const pkgAssets = new Set(pkg.pkg?.assets || []);
if ([...pkgAssets].some(entry => entry === 'package.json')) {
  pass('pkg asset configured: package.json');
} else {
  fail('pkg asset missing: package.json');
}

// Migrations are embedded as JS strings in src/db/migrations/index.js (not .sql
// files on disk) so the migration runner works inside the pkg snapshot.
const migrationsBundle = path.join(ROOT, 'src/db/migrations/index.js');
if (fs.existsSync(migrationsBundle)) {
  pass('migrations bundled: src/db/migrations/index.js exists');
} else {
  fail('migrations bundle missing: src/db/migrations/index.js not found (exe would crash at boot)');
}

// Client assets and hls.js are embedded in src/client-bundle.js (generated by
// scripts/generate-client-bundle.js) so the packaged exe can serve them without
// relying on broken pkg.assets globs.
const clientBundle = path.join(ROOT, 'src/client-bundle.js');
if (fs.existsSync(clientBundle)) {
  pass('client assets bundled: src/client-bundle.js exists');
  try {
    const actual = fs.readFileSync(clientBundle);
    const expected = Buffer.from(buildClientBundleSource().source, 'utf8');
    if (Buffer.compare(actual, expected) === 0) {
      pass('client assets bundled: src/client-bundle.js is current');
    } else {
      fail('client bundle stale: run node scripts/generate-client-bundle.js');
    }
  } catch (err) {
    fail(`client bundle check failed: ${err.message}`);
  }
} else {
  fail('client bundle missing: run node scripts/generate-client-bundle.js');
}

// better_sqlite3.node is embedded by scripts/generate-native-bundle.js into
// src/native-bundle.js (gitignored, generated by build:exe before its release
// check). Verify the source .node file exists so the generator can run.
const sqliteNode = path.join(ROOT, 'node_modules/better-sqlite3/build/Release/better_sqlite3.node');
if (fs.existsSync(sqliteNode)) {
  pass('native binding source: better_sqlite3.node exists');
} else {
  fail('native binding missing: run npm install (or npm rebuild better-sqlite3)');
}

for (const rel of [
  'client/creator.html',
  'client/index.html',
  'node_modules/hls.js/dist/hls.min.js',
  'src/index.js',
  'src/launcher.js',
  'src/native-loader.js',
  'scripts/preflight.js',
  'scripts/check-release-clean.js',
  'scripts/check-migrations.js',
  'scripts/check-scheduler.js',
  'scripts/check-analytics.js',
  'scripts/smoke.js',
  'scripts/smoke-exe.js',
]) {
  if (fs.existsSync(path.join(ROOT, rel))) pass(`required file exists: ${rel}`);
  else fail(`required file missing: ${rel}`);
}

if (!ok) {
  process.exitCode = 1;
} else {
  console.log('Package asset check passed.');
}
