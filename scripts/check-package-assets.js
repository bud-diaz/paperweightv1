#!/usr/bin/env node
// Verifies packaging metadata and required bundle assets before build:exe.

const fs = require('fs');
const path = require('path');
const zlib = require('zlib');
const crypto = require('crypto');
const { buildClientBundleSource } = require('./generate-client-bundle');

const ROOT = path.resolve(__dirname, '..');
let ok = true;

function pass(msg) {
  console.log(`OK   ${msg}`);
}

function fail(msg) {
  console.log(`FAIL ${msg}`);
  ok = false;
}

function readJson(rel) {
  return JSON.parse(fs.readFileSync(path.join(ROOT, rel), 'utf8'));
}

function sha256(buffer) {
  return crypto.createHash('sha256').update(buffer).digest('hex');
}

function sha256File(filepath) {
  return sha256(fs.readFileSync(filepath));
}

function requireFresh(filepath) {
  delete require.cache[require.resolve(filepath)];
  return require(filepath);
}

const pkg = readJson('package.json');
const lock = readJson('package-lock.json');

if (pkg.version === lock.version && pkg.version === lock.packages?.['']?.version) {
  pass(`package version aligned: ${pkg.version}`);
} else {
  fail(`version mismatch: package=${pkg.version}, lock=${lock.version}, root=${lock.packages?.['']?.version}`);
}

// pkg.assets globs are broken for node20 targets. package.json is still needed
// by pkg for version and main resolution.
const pkgAssets = new Set(pkg.pkg?.assets || []);
if ([...pkgAssets].some(entry => entry === 'package.json')) {
  pass('pkg asset configured: package.json');
} else {
  fail('pkg asset missing: package.json');
}

// Migrations are embedded as JS strings in src/db/migrations/index.js (not .sql
// files on disk) so the migration runner works inside the pkg snapshot.
const migrationsBundle = path.join(ROOT, 'src/db/migrations/index.js');
if (fs.existsSync(migrationsBundle)) {
  pass('migrations bundled: src/db/migrations/index.js exists');
} else {
  fail('migrations bundle missing: src/db/migrations/index.js not found (exe would crash at boot)');
}

// Client assets and hls.js are embedded in src/client-bundle.js (generated by
// scripts/generate-client-bundle.js) so the packaged exe can serve them without
// relying on broken pkg.assets globs.
const clientBundle = path.join(ROOT, 'src/client-bundle.js');
if (fs.existsSync(clientBundle)) {
  pass('client assets bundled: src/client-bundle.js exists');
  try {
    const actual = fs.readFileSync(clientBundle);
    const expected = Buffer.from(buildClientBundleSource().source, 'utf8');
    if (Buffer.compare(actual, expected) === 0) {
      pass('client assets bundled: src/client-bundle.js is current');
    } else {
      fail('client bundle stale: run node scripts/generate-client-bundle.js');
    }
  } catch (err) {
    fail(`client bundle check failed: ${err.message}`);
  }
} else {
  fail('client bundle missing: run node scripts/generate-client-bundle.js');
}

// better_sqlite3.node is embedded by scripts/generate-native-bundle.js into
// src/native-bundle.js (gitignored, generated by build:exe before its release
// check). Verify both the source .node file and generated bundle are current.
const sqliteNode = path.join(ROOT, 'node_modules/better-sqlite3/build/Release/better_sqlite3.node');
if (fs.existsSync(sqliteNode)) {
  pass('native binding source: better_sqlite3.node exists');
} else {
  fail('native binding missing: run npm install (or npm rebuild better-sqlite3)');
}

const nativeBundlePath = path.join(ROOT, 'src/native-bundle.js');
if (fs.existsSync(nativeBundlePath)) {
  pass('native binding bundled: src/native-bundle.js exists');
  try {
    const nativeBundle = requireFresh(nativeBundlePath);
    const expectedHash = fs.existsSync(sqliteNode) ? sha256File(sqliteNode) : null;
    if (nativeBundle.platform === process.platform) pass(`native bundle platform: ${process.platform}`);
    else fail(`native bundle platform mismatch: bundle=${nativeBundle.platform || 'unknown'}, runtime=${process.platform}`);

    if (nativeBundle.arch === process.arch) pass(`native bundle arch: ${process.arch}`);
    else fail(`native bundle arch mismatch: bundle=${nativeBundle.arch || 'unknown'}, runtime=${process.arch}`);

    if (nativeBundle.moduleAbi === process.versions.modules) pass(`native bundle ABI: ${process.versions.modules}`);
    else fail(`native bundle ABI mismatch: bundle=${nativeBundle.moduleAbi || 'unknown'}, runtime=${process.versions.modules}`);

    if (expectedHash && nativeBundle.sha256 === expectedHash) pass('native bundle hash is current');
    else if (expectedHash) fail('native bundle stale: run node scripts/generate-native-bundle.js');
  } catch (err) {
    fail(`native bundle check failed: ${err.message}`);
  }
} else {
  fail('native bundle missing: run node scripts/generate-native-bundle.js');
}

// FFmpeg/ffprobe are embedded by scripts/generate-ffmpeg-bundle.js into
// src/ffmpeg-bundle.js because pkg asset globs do not reliably include them.
const ffmpegBundlePath = path.join(ROOT, 'src/ffmpeg-bundle.js');
const ffmpegNames = {
  ffmpeg: process.platform === 'win32' ? 'ffmpeg.exe' : 'ffmpeg',
  ffprobe: process.platform === 'win32' ? 'ffprobe.exe' : 'ffprobe',
};

if (fs.existsSync(ffmpegBundlePath)) {
  pass('FFmpeg bundled: src/ffmpeg-bundle.js exists');
  try {
    const ffmpegBundle = requireFresh(ffmpegBundlePath);
    if (ffmpegBundle.platform === process.platform) pass(`FFmpeg bundle platform: ${process.platform}`);
    else fail(`FFmpeg bundle platform mismatch: bundle=${ffmpegBundle.platform || 'unknown'}, runtime=${process.platform}`);

    if (ffmpegBundle.arch === process.arch) pass(`FFmpeg bundle arch: ${process.arch}`);
    else fail(`FFmpeg bundle arch mismatch: bundle=${ffmpegBundle.arch || 'unknown'}, runtime=${process.arch}`);

    for (const [id, filename] of Object.entries(ffmpegNames)) {
      const vendorFile = path.join(ROOT, 'vendor', 'ffmpeg', filename);
      const entry = ffmpegBundle.binaries?.[id];
      if (!fs.existsSync(vendorFile)) {
        fail(`FFmpeg vendor binary missing: vendor/ffmpeg/${filename}`);
        continue;
      }
      if (!entry) {
        fail(`FFmpeg bundle entry missing: ${id}`);
        continue;
      }
      if (entry.filename === filename) pass(`FFmpeg bundle filename: ${filename}`);
      else fail(`FFmpeg bundle filename mismatch for ${id}: ${entry.filename || 'unknown'}`);

      const source = fs.readFileSync(vendorFile);
      const sourceHash = sha256(source);
      if (entry.size === source.length) pass(`FFmpeg bundle size current: ${filename}`);
      else fail(`FFmpeg bundle size stale for ${filename}`);

      if (entry.sha256 === sourceHash) pass(`FFmpeg bundle hash current: ${filename}`);
      else fail(`FFmpeg bundle hash stale for ${filename}`);

      if (!Array.isArray(entry.data) || !entry.data.length) {
        fail(`FFmpeg bundle data missing for ${filename}`);
        continue;
      }

      const compressed = Buffer.from(entry.data.join(''), 'base64');
      if (entry.compressedSize === compressed.length) pass(`FFmpeg compressed size current: ${filename}`);
      else fail(`FFmpeg compressed size stale for ${filename}`);

      if (entry.compressedSha256 === sha256(compressed)) pass(`FFmpeg compressed hash current: ${filename}`);
      else fail(`FFmpeg compressed hash stale for ${filename}`);

      const inflated = entry.compression === 'gzip' ? zlib.gunzipSync(compressed) : compressed;
      if (Buffer.compare(inflated, source) === 0) pass(`FFmpeg bundle payload current: ${filename}`);
      else fail(`FFmpeg bundle payload stale for ${filename}`);
    }
  } catch (err) {
    fail(`FFmpeg bundle check failed: ${err.message}`);
  }
} else {
  fail('FFmpeg bundle missing: run node scripts/generate-ffmpeg-bundle.js');
}

for (const rel of [
  'client/creator.html',
  'node_modules/hls.js/dist/hls.min.js',
  'src/index.js',
  'src/launcher.js',
  'src/native-loader.js',
  'src/runtime/ffmpeg.js',
  'scripts/generate-client-bundle.js',
  'scripts/generate-native-bundle.js',
  'scripts/generate-ffmpeg-bundle.js',
  'scripts/preflight.js',
  'scripts/check-release-clean.js',
  'scripts/check-migrations.js',
  'scripts/check-scheduler.js',
  'scripts/check-analytics.js',
  'scripts/smoke.js',
  'scripts/smoke-exe.js',
]) {
  if (fs.existsSync(path.join(ROOT, rel))) pass(`required file exists: ${rel}`);
  else fail(`required file missing: ${rel}`);
}

if (!ok) {
  process.exitCode = 1;
} else {
  console.log('Package asset check passed.');
}
